type I18nValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, vars?: TranslationVars) => string;
};

const I18nContext = createContext<I18nValue | null>(null);
const STORAGE_KEY = "shopee-work-language";

export function translateText(language: Language, key: string, vars?: TranslationVars) {
  let result = dictionary[language][key] || key;
  Object.entries(vars ?? {}).forEach(([name, replacement]) => {
    result = result.replaceAll(`{${name}}`, String(replacement));
  });
  return result;
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    return stored === "id" ? "id" : "en";
  });

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language === "id" ? "id" : "en";
  }, [language]);

  const value = useMemo<I18nValue>(() => ({
    language,
    setLanguage,
    t: (key, vars) => translateText(language, key, vars),
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}

export function LanguageSwitcher({ compact = false }: { compact?: boolean }) {
  const { language, setLanguage } = useI18n();
  return <label className={`inline-flex items-center gap-2 rounded-xl border border-slate-200 bg-white ${compact ? "px-2 py-1.5" : "px-3 py-2"}`}>
    <span className="text-xs" aria-hidden="true">🌐</span>
    <span className="sr-only">Language</span>
    <select value={language} onChange={(event) => setLanguage(event.target.value as Language)} aria-label="Language" className="cursor-pointer bg-transparent text-xs font-black text-slate-600 outline-none">
      <option value="en">English</option>
      <option value="id">Bahasa Indonesia</option>
    </select>
  </label>;
}
